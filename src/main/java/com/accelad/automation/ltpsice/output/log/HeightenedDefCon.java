package com.accelad.automation.ltpsice.output.log;

public class HeightenedDefCon {

    private double from;
    private double to;

    public HeightenedDefCon(double from, double to) {
        this.from = from;
        this.to = to;
    }

    public double getFrom() {
        return from;
    }

    public double getTo() {
        return to;
    }

}
